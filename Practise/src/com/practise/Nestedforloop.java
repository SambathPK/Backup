package com.practise;

public class Nestedforloop {
public static void main(String[] args) {
		for (int i = 1; i<=5; i++) {
			for (int j = 1; j <=3; j++) {
				for (int k = 1; k <=3; k++) {
					for (int l = 1; l <=3; l++) {
						System.out.print(i+" ");
					}
					System.out.println();
				}
			System.out.println();
			}
			System.out.println();
		}
	}

}
